#include "../headers/header.h"

void	ft_edit_imax(t_hook_info *info)
{
	if (info->keycode == 124)
		info->f->i_max *= FACTOR_ZOOM - 0.38;
	else if (info->keycode == 123)
		info->f->i_max /= FACTOR_ZOOM - 0.38;
}

/*
 * 123 == left -
 * 124 == right +
*/
